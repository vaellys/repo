package cn.zthz.tool.proxy;

public class RequirementProxyException extends Exception{

	private static final long serialVersionUID = 1L;

	public RequirementProxyException() {
		super();

	}

	public RequirementProxyException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public RequirementProxyException(String message, Throwable cause) {
		super(message, cause);

	}

	public RequirementProxyException(String message) {
		super(message);

	}

	public RequirementProxyException(Throwable cause) {
		super(cause);

	}
}
