package cn.zthz.tool.proxy;

public class UserProxyException extends Exception {
	private static final long serialVersionUID = -9204908695019611765L;

	public UserProxyException() {
		super();

	}

	public UserProxyException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public UserProxyException(String message, Throwable cause) {
		super(message, cause);

	}

	public UserProxyException(String message) {
		super(message);

	}

	public UserProxyException(Throwable cause) {
		super(cause);

	}

}
