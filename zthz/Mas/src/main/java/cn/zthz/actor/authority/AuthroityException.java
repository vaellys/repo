package cn.zthz.actor.authority;

public class AuthroityException extends Exception {
	private static final long serialVersionUID = -717623760536732808L;

	public AuthroityException() {
		super();
	}

	public AuthroityException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AuthroityException(String message, Throwable cause) {
		super(message, cause);
	}

	public AuthroityException(String message) {
		super(message);
	}

	public AuthroityException(Throwable cause) {
		super(cause);
	}

}
