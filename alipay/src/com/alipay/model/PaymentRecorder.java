package com.alipay.model;

import java.math.BigDecimal;
import java.sql.Timestamp;

public class PaymentRecorder {
	private Integer id;
	private String invoice_title;
	private String first_name;
	private String last_name;
	private String confirmation_number;
	private BigDecimal pay_money;
	private Timestamp create_time;
	private String alipay_number;
	private int trade_status;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getInvoice_title() {
		return invoice_title;
	}
	public void setInvoice_title(String invoice_title) {
		this.invoice_title = invoice_title;
	}
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getLast_name() {
		return last_name;
	}
	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	public String getConfirmation_number() {
		return confirmation_number;
	}
	public void setConfirmation_number(String confirmation_number) {
		this.confirmation_number = confirmation_number;
	}
	public BigDecimal getPay_money() {
		return pay_money;
	}
	public void setPay_money(BigDecimal pay_money) {
		this.pay_money = pay_money;
	}
	public Timestamp getCreate_time() {
		return create_time;
	}
	public void setCreate_time(Timestamp create_time) {
		this.create_time = create_time;
	}
	public String getAlipay_number() {
		return alipay_number;
	}
	public void setAlipay_number(String alipay_number) {
		this.alipay_number = alipay_number;
	}
	public int getTrade_status() {
		return trade_status;
	}
	public void setTrade_status(int trade_status) {
		this.trade_status = trade_status;
	}
}
